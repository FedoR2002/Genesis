public class PlaceProp {
    private int sunLevel;
    private int organicLevel;
    private int mineralLevel;
    private int radiationLevel;

    public int getRadiationLevel() {
        return radiationLevel;
    }

    public void setRadiationLevel(int radiationLevel) {
        this.radiationLevel = radiationLevel;
    }

    public int getMineralLevel() {
        return mineralLevel;
    }

    public void setMineralLevel(int mineralLevel) {
        this.mineralLevel = mineralLevel;
    }

    public int getOrganicLevel() {
        return organicLevel;
    }

    public void setOrganicLevel(int organicLevel) {
        this.organicLevel = organicLevel;
    }

    public int getSunLevel() {
        return sunLevel;
    }

    public void setSunLevel(int sunLevel) {
        this.sunLevel = sunLevel;
    }
}
